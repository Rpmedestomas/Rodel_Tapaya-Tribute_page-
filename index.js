// first image
var image_one_modal = document.getElementById("image_one_modal");

var image_one = document.getElementById("image_one");
var image_one_modal_image = document.getElementById("image_one_modal_image");
var image_one_modal_caption = document.getElementById("image_one_modal_caption");
image_one.onclick = function(){
    image_one_modal.style.display = "block";
    image_one_modal_image.src = this.src;
    image_one_modal_caption.innerHTML = this.alt;
}

var image_one_modal_close = document.getElementById("image_one_modal_close");

image_one_modal_close.onclick = function() { 
    image_one_modal.style.display = "none";
}

